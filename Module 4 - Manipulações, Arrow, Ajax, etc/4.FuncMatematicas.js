//Arredonda o número
let num = Math.round(3.47)
console.log(num); // 3

//Arredonda sempre para baixo
let num2 = Math.floor(3.99)
console.log(num2); // 3

//Arredonda sempre para cima
let num3 = Math.ceil(3.99)
console.log(num3); // 4

//Retorna o menor número
let num4 = Math.min(7, 100, 200, 2, 5)
console.log(num4);

//Números aleatórios
let numRandom = Math.random()
console.log(numRandom) * 100;

//Números aleatórios entre 0 e 100
